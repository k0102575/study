package com.theo.Shapes;

public class Straight extends Shape {
    public Straight(int x1, int y1, int x2, int y2) {
        super.setX1(x1);
        super.setY1(y1);
        super.setX2(x2);
        super.setY2(y2);
    }

    @Override
    public int getX1() {
        return super.getX1();
    }

    @Override
    public void setX1(int x1) {
        super.setX1(x1);
    }

    @Override
    public int getY1() {
        return super.getY1();
    }

    @Override
    public void setY1(int y1) {
        super.setY1(y1);
    }

    @Override
    public int getX2() {
        return super.getX2();
    }

    @Override
    public void setX2(int x2) {
        super.setX2(x2);
    }

    @Override
    public int getY2() {
        return super.getY2();
    }

    @Override
    public void setY2(int y2) {
        super.setY2(y2);
    }
}
